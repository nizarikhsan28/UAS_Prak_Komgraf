# Mengambil 5 baris teratas
top_5_dividends = data_stock_dividendShare.head(5)[['Stock Name', 'Symbol', 'Dividend/Share']]

# Menampilkan hasil, 5 data
print("Atribut Stock Name dan Symbol dengan Dividen/Share 5 tertinggi:")
print(top_5_dividends)

