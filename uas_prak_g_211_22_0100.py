import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
# Membaca file CSV
data_stock = pd.read_csv('stock.csv', sep=';')
print(data_stock.head(13))  # Menampilkan 13 data yang ada dalam file stock.csv

"""**A. Distribusi data pada setiap atribut Purchase Price, Current Price dan Gain/Loss dengan
grafik histogram**
"""

# Menampilkan histogram untuk atribut "Purchase Price" menggunakan Seaborn
plt.figure(figsize=(20, 4))
sns.histplot(data=data_stock, x='Purchase Price', kde=True, edgecolor='white', color='yellow')
plt.xlabel('Purchase Price')
plt.ylabel('Count')
plt.title('Histogram of Purchase Price')
plt.show()

# Menampilkan histogram untuk atribut "Current Price" menggunakan Seaborn
plt.figure(figsize=(20, 4))
sns.histplot(data=data_stock, x='Current Price', kde=True, edgecolor='white', color='yellow')
plt.xlabel('Current Price')
plt.ylabel('Count')
plt.title('Histogram of Current Price')
plt.show()

# Menampilkan histogram untuk atribut "Gain/Loss" menggunakan Seaborn
plt.figure(figsize=(20, 4))
sns.histplot(data=data_stock, x='Gain/Loss', kde=True, edgecolor='white', color='yellow')
plt.xlabel('Gain/Loss')
plt.ylabel('Count')
plt.title('Histogram of Gain/Loss')
plt.show()

"""**B. Atribut Stock Name dan Symbol yang memiliki Dividen / Share 5 tertinggi**"""

# Mengurutkan data berdasarkan kolom "Dividen/Share" secara menurun
